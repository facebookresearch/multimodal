import torch
import torch.nn.functional as F
from torch import nn
from torchmultimodal.utils.common import shift_dim
from torchvision.transforms.functional import normalize, resize


class S3DEncoder(nn.Module):
    """
    Encode videos to a fixed size vector

    Args:
        trainable (bool): true if model weights should be trained
        preprocess_mean (sequence): sequence of means to normalize each channel to
        preprocess_std (sequence): sequence of standard deviations to normalize each channel to
    """

    def __init__(
        self,
        trainable,
        preprocess_mean=(0.43216, 0.394666, 0.37645),
        preprocess_std=(0.22803, 0.22145, 0.216989),
    ):
        super().__init__()
        self.preprocess_mean = preprocess_mean
        self.preprocess_std = preprocess_std
        self.model = S3D(400)
        self.embedding_dim = list(self.model.fc.children())[0].in_channels
        self.model.fc = nn.Identity()
        for p in self.model.parameters():
            p.requires_grad = trainable

    def preprocess(self, x, time_samples=32):
        b, t, h, w, c = x.shape
        if t != time_samples:
            x = F.interpolate(
                shift_dim(x, -1, 1), size=[time_samples, h, w]
            )  # "b t h w c -> b c t h w"
            x = shift_dim(x, 1, -1)  # "b c t h w -> b t h w c"
        assert c == 3
        x = x.flatten(start_dim=0, end_dim=1)  # "b t h w c -> (b t) h w c"
        x = shift_dim(x, -1, 1)  # "(b t) h w c -> (b t) c h w"
        x = resize(x, (224, 224)) if h != 224 and w != 224 else x
        # this is a rgb video, just normalize
        x = x.float() / 255.0
        # convert to BCTHW
        x = normalize(x, mean=self.preprocess_mean, std=self.preprocess_std)
        x = x.unflatten(dim=0, sizes=(b, time_samples))  # "(b t) c h w -> b t c h w"
        x = shift_dim(x, 2, 1)  # "b t c h w -> b c t h w"
        return x

    def forward(self, x):
        x = self.preprocess(x)
        return self.model(x)


class S3D(nn.Module):
    def __init__(self, num_class):
        super(S3D, self).__init__()
        self.base = nn.Sequential(
            SepConv3d(3, 64, kernel_size=7, stride=2, padding=3),
            nn.MaxPool3d(kernel_size=(1, 3, 3), stride=(1, 2, 2), padding=(0, 1, 1)),
            BasicConv3d(64, 64, kernel_size=1, stride=1),
            SepConv3d(64, 192, kernel_size=3, stride=1, padding=1),
            nn.MaxPool3d(kernel_size=(1, 3, 3), stride=(1, 2, 2), padding=(0, 1, 1)),
            Mixed3b(),
            Mixed3c(),
            nn.MaxPool3d(kernel_size=(3, 3, 3), stride=(2, 2, 2), padding=(1, 1, 1)),
            Mixed4b(),
            Mixed4c(),
            Mixed4d(),
            Mixed4e(),
            Mixed4f(),
            nn.MaxPool3d(kernel_size=(2, 2, 2), stride=(2, 2, 2), padding=(0, 0, 0)),
            Mixed5b(),
            Mixed5c(),
        )
        self.fc = nn.Sequential(
            nn.Conv3d(1024, num_class, kernel_size=1, stride=1, bias=True),
        )

    def forward(self, x):
        y = self.base(x)
        y = F.avg_pool3d(y, (2, y.size(3), y.size(4)), stride=1)
        y = self.fc(y)
        y = y.view(y.size(0), y.size(1), y.size(2))
        logits = torch.mean(y, 2)

        return logits


class BasicConv3d(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_size, stride, padding=0):
        super(BasicConv3d, self).__init__()
        self.conv = nn.Conv3d(
            in_planes,
            out_planes,
            kernel_size=kernel_size,
            stride=stride,
            padding=padding,
            bias=False,
        )
        self.bn = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = self.conv(x)
        x = self.bn(x)
        x = self.relu(x)
        return x


class SepConv3d(nn.Module):
    def __init__(self, in_planes, out_planes, kernel_size, stride, padding=0):
        super(SepConv3d, self).__init__()
        self.conv_s = nn.Conv3d(
            in_planes,
            out_planes,
            kernel_size=(1, kernel_size, kernel_size),
            stride=(1, stride, stride),
            padding=(0, padding, padding),
            bias=False,
        )
        self.bn_s = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu_s = nn.ReLU()

        self.conv_t = nn.Conv3d(
            out_planes,
            out_planes,
            kernel_size=(kernel_size, 1, 1),
            stride=(stride, 1, 1),
            padding=(padding, 0, 0),
            bias=False,
        )
        self.bn_t = nn.BatchNorm3d(out_planes, eps=1e-3, momentum=0.001, affine=True)
        self.relu_t = nn.ReLU()

    def forward(self, x):
        x = self.conv_s(x)
        x = self.bn_s(x)
        x = self.relu_s(x)

        x = self.conv_t(x)
        x = self.bn_t(x)
        x = self.relu_t(x)
        return x


class Mixed3b(nn.Module):
    def __init__(self):
        super(Mixed3b, self).__init__()

        self.branch0 = nn.Sequential(
            BasicConv3d(192, 64, kernel_size=1, stride=1),
        )
        self.branch1 = nn.Sequential(
            BasicConv3d(192, 96, kernel_size=1, stride=1),
            SepConv3d(96, 128, kernel_size=3, stride=1, padding=1),
        )
        self.branch2 = nn.Sequential(
            BasicConv3d(192, 16, kernel_size=1, stride=1),
            SepConv3d(16, 32, kernel_size=3, stride=1, padding=1),
        )
        self.branch3 = nn.Sequential(
            nn.MaxPool3d(kernel_size=(3, 3, 3), stride=1, padding=1),
            BasicConv3d(192, 32, kernel_size=1, stride=1),
        )

    def forward(self, x):
        x0 = self.branch0(x)
        x1 = self.branch1(x)
        x2 = self.branch2(x)
        x3 = self.branch3(x)
        out = torch.cat((x0, x1, x2, x3), 1)

        return out


